public class Tool {

    private String name;
    private String effect;

    public Tool(String name, String effect) {
        this.name = name;
        this.effect = effect;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEffect() {
        return this.effect;
    }

    public void setEffect(String effect) {
        this.effect = effect;
    }

}